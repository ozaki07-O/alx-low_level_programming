#ifndef _MAIN_H_
#define _MAIN_H_
/**
 * _strcat - concatenates two strings
 * @dest: first string
 * @src: second string
 *
 * Return: a pointer to string dest
 */
char *_strcat(char *dest, char *src);

/**
 * _strncat - concatenates two strings
 * @dest: first string
 * @src: second string
 * @n: amount of times
 *
 * Return: a pointer to string dest
 */
char *_strncat(char *dest, char *src, int n);

#endif
